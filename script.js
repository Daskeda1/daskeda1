function setDirection(lang) {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("ltr", lang === "en");
}

function applyTranslations(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      const value = translations[lang][key];
      if (value.includes("\n")) {
        el.innerHTML = value.replace(/\n/g, "<br>");
      } else {
        el.textContent = value;
      }
    }
  });
}

function updateToggle(lang) {
  const toggle = document.getElementById("langToggle");
  if (toggle) {
    toggle.textContent = lang === "ar" ? "EN" : "AR";
  }
}

function applyLanguage(lang) {
  const safeLang = lang === "en" ? "en" : "ar";
  localStorage.setItem("siteLang", safeLang);
  setDirection(safeLang);
  applyTranslations(safeLang);
  updateToggle(safeLang);
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("siteLang") || "ar";
  applyLanguage(savedLang);

  const toggle = document.getElementById("langToggle");
  if (toggle) {
    toggle.addEventListener("click", () => {
      const currentLang = localStorage.getItem("siteLang") || "ar";
      const newLang = currentLang === "ar" ? "en" : "ar";
      applyLanguage(newLang);
    });
  }
});